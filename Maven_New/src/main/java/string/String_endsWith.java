package string;

public class String_endsWith 
{

	public static void main(String[] args) 
	{
		String s1="Hello World";
		System.out.println(s1.endsWith("ld"));
		System.out.println(s1.endsWith("Ld"));
		System.out.println(s1.endsWith("wo"));
		System.out.println(s1.endsWith("Ld"));
	}

}
